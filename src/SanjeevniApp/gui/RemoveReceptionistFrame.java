    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String docId = jcrecepId.getSelectedItem().toString();
       try{ 
           boolean result = ReceptionistDao.deleteReceptionistById(docId);
       
        if(result){
            JOptionPane.showMessageDialog(null, "Receptionist removed successfully!");
            jcrecepId.removeItem(docId);
            if(jcrecepId.getItemCount() == 0){
                btnDelete.setEnabled(false);
                JOptionPane.showMessageDialog(null, "All Receptionist removed!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Deletion unsuccessful");
        }
       }  catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
       }
    }//GEN-LAST:event_btnDeleteActionPerformed

